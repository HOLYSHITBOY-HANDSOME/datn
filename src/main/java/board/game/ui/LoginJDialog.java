    private void LoginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginbtnActionPerformed
        // TODO add your handling code here:
        String user = txtUserName.getText().trim();
        String pass = new String(txtPassWord.getText());

        if (user.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß t√†i kho·∫£n v√† m·∫≠t kh·∫©u!");
            return;
        }

        try {
            String sql = "SELECT * FROM Users WHERE tennguoidung = ? AND matkhau = ?";
            ResultSet rs = XJdbc.executeQuery(sql, user, pass);

            if (rs.next()) {
                boolean isActive = rs.getBoolean("trangthai"); // Ki·ªÉm tra tr·∫°ng th√°i ho·∫°t ƒë·ªông

                if (!isActive) {
                    JOptionPane.showMessageDialog(this, "T√†i kho·∫£n n√†y ƒë√£ b·ªã d·ª´ng ho·∫°t ƒë·ªông!");
                    return; // Kh√¥ng cho ti·∫øp t·ª•c ƒëƒÉng nh·∫≠p
                }

                // N·∫øu t√†i kho·∫£n c√≤n ho·∫°t ƒë·ªông th√¨ ti·∫øp t·ª•c x·ª≠ l√Ω
                this.userId = rs.getString("idnguoidung");
                fullName = rs.getString("tennguoidung");
                isManager = rs.getInt("vaitro") == 1; // 1 l√† admin

                loginSuccessful = true;
                JOptionPane.showMessageDialog(this, "ƒêƒÉng nh·∫≠p th√†nh c√¥ng!");
                dispose(); // ƒê√≥ng form login
            } else {
                JOptionPane.showMessageDialog(this, "Sai t√†i kho·∫£n ho·∫∑c m·∫≠t kh·∫©u!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L·ªói k·∫øt n·ªëi: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_LoginbtnActionPerformed

